    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String inputNumberOfCylinders = iNumberOfCylinders.getText();
        String inputCylinderCapacity = iCylinderCapacity.getText();
        String fuel = iFuel.getText();
        String inputNumberOfSeat = iNumberOfSeat.getText();

        if (inputNumberOfCylinders.isBlank() || inputCylinderCapacity.isBlank() || fuel.isBlank() || inputNumberOfSeat.isBlank()) {
            JOptionPane.showMessageDialog(null, "Gagal menambah data, ada yang kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int numberOfCylinders = Integer.parseInt(inputNumberOfCylinders);
            int cylinderCapacity = Integer.parseInt(inputCylinderCapacity);
            int numberOfSeat = Integer.parseInt(inputNumberOfSeat);

            Mesin engine = new Mesin(numberOfCylinders, cylinderCapacity);
            Mobil car = new Mobil(fuel, engine, numberOfSeat);

            Connection link = Database.getConnection();

            String query = "INSERT INTO mobil(number_of_cylinders, cylinder_capacity, fuel, number_of_seat) VALUES(?, ?, ?, ?)";

            PreparedStatement statement = link.prepareStatement(query);

            statement.setInt(1, car.getEngine().numberOfCylinders);
            statement.setInt(2, car.getEngine().cylinderCapacity);
            statement.setString(3, car.getFuel());
            statement.setInt(4, car.getNumberOfSeat());

            statement.execute();

            iNumberOfCylinders.setText("");
            iCylinderCapacity.setText("");
            iFuel.setText("");
            iNumberOfSeat.setText("");

            loadData();

            JOptionPane.showMessageDialog(null, "Berhasil menambah data!");
        } catch (NumberFormatException | SQLException e) {
            System.out.println(e);

            JOptionPane.showMessageDialog(null, "Gagal menambah data!, " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

