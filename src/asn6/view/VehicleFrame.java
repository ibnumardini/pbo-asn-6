    private void loadData() {
        model.setRowCount(0);

        try {
            Connection link = Database.getConnection();

            Statement statement = link.createStatement();

            String query = "SELECT number_of_cylinders, cylinder_capacity, fuel, number_of_seat FROM mobil";

            ResultSet chunk = statement.executeQuery(query);

            while (chunk.next()) {
                int numberOfCylinders = chunk.getInt("number_of_cylinders");
                int cylinderCapacity = chunk.getInt("cylinder_capacity");
                String fuel = chunk.getString("fuel");
                int numberOfSeat = chunk.getInt("number_of_seat");

                Mesin engine = new Mesin(numberOfCylinders, cylinderCapacity);
                Mobil car = new Mobil(fuel, engine, numberOfSeat);

                Object[] data = {
                    car.getEngine().numberOfCylinders,
                    car.getEngine().cylinderCapacity,
                    car.getFuel(),
                    car.getNumberOfSeat()
                };

                model.addRow(data);
            }

        } catch (SQLException e) {
            System.out.println(e);
        }
    }

